var resCount = 0;
var resLoad = 0;

function loadAllResources() {
    for (let image in images) {
        let img = new Image();
        img.src = images[image];
        img.onload = () => {
            resLoad++;
            scene.controls[0].text = "Loading resources...";
            scene.controls[1].text = "images/" + image + " " + resLoad + "/" + resCount;
            if (resLoad == resCount) {
                scene.controls[0].text = "Everything good to go!";
                scene.controls[2].clickthrough = scene.controls[3].clickthrough = scene.controls[4].clickthrough = false;
                scene.controls[2].alpha = scene.controls[3].alpha = scene.controls[4].alpha = 1;
            }
        }
        images[image] = img;
        resCount++;
    }
    for (let scn in scenes) {
        let scr = document.createElement("script");
        scr.src = scenes[scn];
        scr.onload = () => {
            resLoad++;
            scene.controls[0].text = "Loading resources...";
            scene.controls[1].text = "scenes/" + scn + " " + resLoad + "/" + resCount;
            if (resLoad == resCount) {
                scene.controls[0].text = "Everything good to go!";
                scene.controls[2].clickthrough = scene.controls[3].clickthrough = scene.controls[4].clickthrough = false;
                scene.controls[2].alpha = scene.controls[3].alpha = scene.controls[4].alpha = 1;
            }
        }
        scenes[scn] = scr;
        document.head.appendChild(scr);
        resCount++;
    }
    for (let snd in audio) {
        let aud = new Audio(audio[snd]);
        aud.onloadeddata = () => {
            resLoad++;
            scene.controls[0].text = "Loading resources...";
            scene.controls[1].text = "audio/" + snd + " " + resLoad + "/" + resCount;
            if (resLoad == resCount) {
                scene.controls[0].text = "Everything good to go!";
                scene.controls[2].clickthrough = scene.controls[3].clickthrough = scene.controls[4].clickthrough = false;
                scene.controls[2].alpha = scene.controls[3].alpha = scene.controls[4].alpha = 1;
            }
        }
        audio[snd] = aud;
        resCount++;
    }
    for (let map in maps) {
        let mp = document.createElement("script");
        mp.src = maps[map];
        mp.onload = () => {
            resLoad++;
            scene.controls[0].text = "Loading resources...";
            scene.controls[1].text = "maps/" + map + " " + resLoad + "/" + resCount;
            if (resLoad == resCount) {
                scene.controls[0].text = "Everything good to go!";
                scene.controls[2].clickthrough = scene.controls[3].clickthrough = scene.controls[4].clickthrough = false;
                scene.controls[2].alpha = scene.controls[3].alpha = scene.controls[4].alpha = 1;
            }
        }
        maps[map] = mp;
        document.head.appendChild(mp);
        resCount++;
    }
}
